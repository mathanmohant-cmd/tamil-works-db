          <p><em>முயங்குதொறும்முயங்குதொறும் உயங்க முகந்து கொண்டு</em> — அகம் 328/10</p>
          <p class="commentary">இவை தனித்தனிச் சொற்களாகவே கொள்ளப்பட்டுள்ளன.</p>
        </div>
      </section>

