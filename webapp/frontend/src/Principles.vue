          <p><em>உறு துயர் அவலமொடு உயிர் செல சாஅய்</em> — ஐங் 313/2</p>
          <p><em>உறு முரண் கடந்த ஆற்றல்</em> — புறம் 135/21</p>
        </div>

        <p><strong>2. தனிவினையாக:</strong></p>
        <div class="example-block">
