          <p class="commentary">பொருள்கள்: அனுபவி (experience), நிலையை அடை, ஏற்படு, ஒன்றினால் பாதிக்கப்படு, படு, தொடு, அடை, பெறு, நிகழ், சம்பவி, போன்றிரு, எதிர்ப்படு, மிகு, பொருந்து, இணை</p>
